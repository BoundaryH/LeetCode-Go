func sampleStats(count []int) []float64 {
	min, max, mode, cSum, sum := 255, 0, 0, 0, float64(0)
	for n, c := range count {
		if c > 0 {
			max = n
			if n < min {
				min = n
			}
		}
		if c > count[mode] {
			mode = n
		}
		cSum += c
		sum += float64(n) * float64(c)
	}
	l, c := 0, 0
	for ; c+count[l] < cSum/2; l++ {
		c += count[l]
	}
	r := l
	if cSum%2 == 0 {
		for ; c+count[r] < cSum/2+1; r++ {
			c += count[r]
		}
	}
	median := float64(l+r) / 2
	return []float64{float64(min), float64(max), sum / float64(cSum), median, float64(mode)}
}

